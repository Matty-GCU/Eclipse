package chapter4;

public class H2_Lader {
	double above, bottom, height, area;
	void setAbove(double a) {
		above = a;
	}
	void setBottom(double a) {
		bottom = a;
	}
	void setHeight(double a) {
		height = a;
	}
	double Area() {
		return area = (above + bottom) * height /2;
	}
}
